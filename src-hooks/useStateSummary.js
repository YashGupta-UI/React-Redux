/*

Summary- useState hook

1. The useState hook let you add state to functional components

2. In classes, the state is always on object, with the useState hooks the state doesn't have to be an object

3. The useState hooks returns an array with 2 elements, the first element is the current value of the state
and the second element is a setter function

4. In case, new state value depends on the previous state value, you can pass a function to the
setter function

5. When dealing with objects or arrays, always make sure to spread your state variable and then call
the setter function


*/
